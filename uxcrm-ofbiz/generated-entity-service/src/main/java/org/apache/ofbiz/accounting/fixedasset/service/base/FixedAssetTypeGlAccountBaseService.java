	/**
	 * Count FixedAssetTypeGlAccounts
	 */
	public Integer count(EntityConditionList conditions) {
		In in = new In();
		in.setEntityName(FixedAssetTypeGlAccount.NAME);
		if (conditions == null) {
			in.setNoConditionFind(OfbizUtil.Y);
		} else {
			in.setEntityConditionList(conditions);
		}
		Out out = executeFindService.runSync(in);
		return out.getListSize();
	}

	/**
	 * Find FixedAssetTypeGlAccounts
	 */
	public List<FixedAssetTypeGlAccount> find(Integer start, Integer number,
			List<String> orderBy, EntityConditionList conditions) {
		List<FixedAssetTypeGlAccount> entityList = Collections.emptyList();
		In in = new In();
		in.setEntityName(FixedAssetTypeGlAccount.NAME);
		if (start == null) {
			start = OfbizUtil.DEFAULT_FIND_START;
		}
		if (number == null) {
			number = OfbizUtil.DEFAULT_FIND_NUMBER;
		}
		in.setOrderByList(orderBy);
		if (conditions == null) {
			in.setNoConditionFind(OfbizUtil.Y);
		} else {
			in.setEntityConditionList(conditions);
		}
		Out out = executeFindService.runSync(in);
		try {
			if (out.getListIt() != null) {
				entityList = FixedAssetTypeGlAccount.fromValues(out.getListIt()
						.getPartialList(start, number));
				out.getListIt().close();
			}
		} catch (GenericEntityException e) {
			log.error(e.getMessage(), e);
		}
		return entityList;
	}

	/**
	 * Find one FixedAssetTypeGlAccount
	 */
	public Optional<FixedAssetTypeGlAccount> findOne(Object fixedAssetTypeId,
			Object fixedAssetId, Object organizationPartyId) {
		List<FixedAssetTypeGlAccount> entityList = null;
		In in = new In();
		in.setEntityName(FixedAssetTypeGlAccount.NAME);
		in.setEntityConditionList(new EntityConditionList<>(Arrays.asList(
				new EntityExpr("fixedAssetTypeId", EntityOperator.EQUALS,
						fixedAssetTypeId), new EntityExpr("fixedAssetId",
						EntityOperator.EQUALS, fixedAssetId), new EntityExpr(
						"organizationPartyId", EntityOperator.EQUALS,
						organizationPartyId)), EntityOperator.AND));
		Out out = executeFindService.runSync(in);
		try {
			if (out.getListIt() != null) {
				entityList = FixedAssetTypeGlAccount.fromValues(out.getListIt()
						.getCompleteList());
				out.getListIt().close();
			}
		} catch (GenericEntityException e) {
			log.error(e.getMessage(), e);
		}
		if (CollectionUtils.isNotEmpty(entityList)) {
			return Optional.of(entityList.get(0));
		}
		return Optional.empty();
	}

